"""
WRITTEN ANSWERS:
1. Easiest format: Parquet. It requires no options and preserves types/schemas perfectly.
2. Most cleaning: CSV. It required manual schema definition, null value handling, and complex boolean logic.
3. Best choice: Parquet. It is significantly faster and stores metadata natively.
"""

# ==============================================================================
# PART 6: Unified Output
# ==============================================================================

# Create dummy artist IDs for CSV to match JSON
df_artists_final = df_artists_clean.select(
    col("artist_name"),
    col("genre"),
    col("country").alias("artist_country"),
    col("followers")
).withColumn("artist_id", concat(lit("ART-"), lpad(monotonically_increasing_id().cast("string"), 5, "0")))

df_enriched_listens = df_events_enriched \
    .join(df_users.select("user_id", "country", "subscription_tier", "age"), on="user_id", how="left") \
    .join(df_artists_final, on="artist_id", how="left")

# Write Partitioned Output
df_enriched_listens.coalesce(4) \
    .write.parquet(
        "data/unified_listening_analytics",
        mode="overwrite",
        partitionBy=["event_month"],
        compression="snappy"
    )

print(f"\n✅ Unified dataset saved to data/unified_listening_analytics")
spark.read.parquet("data/unified_listening_analytics").show(5)

spark.stop()

'''
Key Differences in Data Formats
CSV (Comma Separated Values): A row-based text format. It has no metadata, so Spark must guess the schema or be told explicitly. It is slow to parse and does not support nested data.

JSON (JavaScript Object Notation): A row-based semi-structured format. It supports nesting but is verbose (repeats keys for every row). Spark must scan the file to determine the schema, which is computationally expensive.

Parquet: A columnar storage format. It is highly compressed and stores the schema in the file footer. It supports predicate pushdown and column pruning, allowing Spark to read only the specific data requested, making it the industry standard for analytical workloads.
'''