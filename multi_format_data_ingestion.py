from pyspark.sql import SparkSession
from pyspark.sql.types import *
from pyspark.sql.functions import *
import time
import os
import random

# ==============================================================================
# PART 1: Set Up and Create Test Data
# ==============================================================================

spark = SparkSession.builder \
    .appName("StreamPulse-DataIngestion") \
    .master("local[*]") \
    .config("spark.driver.memory", "2g") \
    .config("spark.sql.shuffle.partitions", "8") \
    .getOrCreate()

# 1a. Generate Artist Catalog (CSV)
random.seed(42)
genres = ["Pop", "Rock", "Hip-Hop", "Jazz", "Classical", "Electronic", "R&B", "Country"]
countries = ["US", "UK", "DE", "JP", "BR", "KR", "FR", "AU", "NG", "IN"]

artist_data = []
for i in range(50000):
    name = f"Artist_{i+1:05d}"
    genre = random.choice(genres)
    country = random.choice(countries)
    followers = random.randint(100, 5000000)
    monthly_listeners = random.randint(50, 2000000)
    verified = random.choice(["true", "false", "TRUE", "False", "yes", ""])
    join_date = f"202{random.randint(0,4)}-{random.randint(1,12):02d}-{random.randint(1,28):02d}"
    if random.random() < 0.02: followers = "NULL"
    if random.random() < 0.01: name = f'Artist "Special" {i}'
    artist_data.append((name, genre, country, str(followers), str(monthly_listeners), verified, join_date))

df_artists_raw = spark.createDataFrame(artist_data, ["artist_name", "genre", "country", "followers", "monthly_listeners", "verified", "join_date"])
df_artists_raw.write.csv("data/artists_csv", header=True, mode="overwrite", quote='"', escape='"')

# 1b. Generate Listening Events (JSON)
event_data = []
for i in range(100000):
    event_data.append({
        "event_id": f"EVT-{i+1:07d}",
        "user_id": f"USR-{random.randint(1, 20000):06d}",
        "artist_id": f"ART-{random.randint(1, 50000):05d}",
        "track_id": f"TRK-{random.randint(1, 200000):06d}",
        "duration_seconds": random.randint(10, 400),
        "completed": random.choice([True, False]),
        "timestamp": f"2025-{random.randint(1,6):02d}-{random.randint(1,28):02d}T{random.randint(0,23):02d}:{random.randint(0,59):02d}:00Z",
        "device": random.choice(["mobile", "desktop", "smart_speaker", "tablet"]),
        "quality": random.choice(["low", "standard", "high", "lossless"]),
    })
spark.createDataFrame(event_data).write.json("data/events_json", mode="overwrite")

# 1c. Generate User Profiles (Parquet)
user_data = [(f"USR-{i+1:06d}", f"user_{i+1}@email.com", random.choice(countries), random.choice(["free", "premium", "family", "student"]), random.randint(16, 75), f"202{random.randint(0,4)}-{random.randint(1,12):02d}-{random.randint(1,28):02d}") for i in range(20000)]
spark.createDataFrame(user_data, ["user_id", "email", "country", "subscription_tier", "age", "signup_date"]).write.parquet("data/users_parquet", mode="overwrite")

# ==============================================================================
# PART 2: Read the Artist Catalog (CSV)
# ==============================================================================

# Task 2a: Read without options
df_bad = spark.read.csv("data/artists_csv")
print("\n--- Task 2a: Bad CSV Read ---")
df_bad.show(5)
"""
Analysis of Task 2a:
- Column names: Not detected (treated as data in first row).
- Data types: Everything is StringType.
- First row: Contains headers.
"""

# Task 2c: Explicit Schema vs inferSchema
artist_schema = StructType([
    StructField("artist_name", StringType(), True),
    StructField("genre", StringType(), True),
    StructField("country", StringType(), True),
    StructField("followers", IntegerType(), True),
    StructField("monthly_listeners", IntegerType(), True),
    StructField("verified", StringType(), True),
    StructField("join_date", DateType(), True),
])

start = time.time()
_ = spark.read.csv("data/artists_csv", header=True, inferSchema=True, nullValue="NULL").count()
time_infer = time.time() - start

start = time.time()
df_explicit = spark.read.csv("data/artists_csv", header=True, schema=artist_schema, nullValue="NULL", dateFormat="yyyy-MM-dd")
_ = df_explicit.count()
time_explicit = time.time() - start

# Task 2d: Clean "verified" column
df_artists_clean = df_explicit.withColumn(
    "verified",
    when(lower(col("verified")).isin("true", "yes"), True)
    .when(lower(col("verified")).isin("false", "no", ""), False)
    .otherwise(None)
    .cast("boolean")
)

# ==============================================================================
# PART 3: Read Listening Events (JSON)
# ==============================================================================

start = time.time()
df_events = spark.read.json("data/events_json")
time_json = time.time() - start

df_events_enriched = df_events.withColumn(
    "event_timestamp", to_timestamp(col("timestamp"), "yyyy-MM-dd'T'HH:mm:ss'Z'")
).withColumn("event_date", to_date(col("event_timestamp"))) \
 .withColumn("event_hour", hour(col("event_timestamp"))) \
 .withColumn("event_month", month(col("event_timestamp"))) \
 .drop("timestamp")

# ==============================================================================
# PART 4: Read User Profiles (Parquet)
# ==============================================================================

start = time.time()
df_users = spark.read.parquet("data/users_parquet")
time_parquet = time.time() - start

# Demonstration of column pruning speed
start = time.time()
_ = spark.read.parquet("data/users_parquet").count()
time_all = time.time() - start

start = time.time()
_ = spark.read.parquet("data/users_parquet").select("user_id", "subscription_tier").count()
time_two = time.time() - start

